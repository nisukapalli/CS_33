/* 
 * twosComp2SignMag - Convert from two's complement to sign-magnitude 
 *   where the MSB is the sign bit
 *   You can assume that x > TMin
 *   Example: twosComp2SignMag(-5) = 0x80000005.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 4
 */
int twosComp2SignMag(int x) {
  /* if x is negative, find the absolute value and add a sign bit */
  int xSign = x >> 31;
  int negX = ~x + 1;
  int addSign = xSign & (negX | (xSign << 31));
  return (~xSign & x) | addSign;
}
/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
  /* if x is negative eliminate excess 1s, then set count to 2 because
 * of the first sign bit and for changing negative to positive, then do
 * binary search by looking for the 1s in the two halves of 16 bits,
 * then 8, 4, 2, and 1 bits */
  int y = x ^ (x >> 31);
  int count = 2;
  int power4, power3, power2, power1, power0;
  int mask;
  power4 = !!(y >> 16) << 4;
  y = y >> power4;
  power3 = !!(y >> 8) << 3;
  y = y >> power3;
  power2 = !!(y >> 4) << 2;
  y = y >> power2;
  power1 = !!(y >> 2) << 1;
  y = y >> power1;
  power0 = !!(y >> 1);
  y = !y;
  count = count + power4 + power3 + power2 + power1 + power0;
  mask = count & (y + 0xFF);
  return mask | y;
}
