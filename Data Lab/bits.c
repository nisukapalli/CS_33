/* 
 * isEqual - return 1 if x == y, and 0 otherwise 
 *   Examples: isEqual(5,5) = 1, isEqual(4,5) = 0
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int isEqual(int x, int y) {
  /* return 1 if x-y is 0, else return 0 */
  int diff = x + (~y + 1); 
  return !diff;
}
/* 
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
  /* extract nth and mth bytes, clear the bits, then copy the extracted
 * bytes back in each other's positions */
  int y = x;
  int temp = ~( (0xFF << (n << 3)) | (0xFF << (m << 3)) );
  int ntemp = 0xFF & (x >> (n << 3));
  int mtemp = 0xFF & (x >> (m << 3));
  y = y & temp;
  ntemp = ntemp << (m << 3);
  mtemp = mtemp << (n << 3);
  y = y | (ntemp | mtemp);
  return y;
}
