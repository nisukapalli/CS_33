/* 
 * subtractionOK - Determine if can compute x-y without overflow
 *   Example: subtractionOK(0x80000000,0x80000000) = 1,
 *            subtractionOK(0x80000000,0x70000000) = 0, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int subtractionOK(int x, int y) {
  /* treat x-y as x+(-y), there is overflow if x and -y have the same sign
 * but x+(-y) has a different sign */

  int xSign = (x >> 31) & 1;
  int ySign = (y >> 31) & 1;
  int diff = x + (~y + 1);
  int diffSign = (diff >> 31) & 1;
  int compare = xSign ^ ySign;
  int notSame = xSign ^ diffSign;
  return !(notSame & compare);
}
/* 
 * isGreater - if x > y  then return 1, else return 0 
 *   Example: isGreater(4,5) = 0, isGreater(5,4) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
  /* determine if x and y are not equal and x-y is positive, if x and y
 * have the same sign or if x is positive and y is negative */
  int xSign = (x >> 31) & 1;
  int ySign = (y >> 31) & 1;
  int diffSign = ((x + (~y + 1)) >> 31) & 1;
  int equal = !(x ^ y);
  int sameSign = !(xSign ^ ySign);
  int notSame = (ySign & (!sameSign));
  int same = sameSign & (!diffSign);
  return (((!equal) & same) | notSame);
}
/*
 * multFiveEighths - multiplies by 5/8 rounding toward 0.
 *   Should exactly duplicate effect of C expression (x*5/8),
 *   including overflow behavior.
 *   Examples: multFiveEighths(77) = 48
 *             multFiveEighths(-22) = -13
 *             multFiveEighths(1073741824) = 13421728 (overflow)
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 3
 */
int multFiveEighths(int x) {
  /* use left shift and addition to multiply by 5, right shift to divide by 8,
 * add a bias if x is negative to round toward 0 */
  int fivex = (x << 2) + x;
  int bias = (fivex >> 31) & 7;
  return ((fivex + bias) >> 3);
}
